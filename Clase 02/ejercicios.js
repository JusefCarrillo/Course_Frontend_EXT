//Comprobacion de numeros pares e impares, si es par o no
let numberRandom = Math.floor(Math.random() *  100);
let textResult;
if (numberRandom % 2 == 0) {
    textResult = "El numero:"+ numberRandom+" es par";
} else {
    textResult = "El numero:"+ numberRandom+" es inpar";
}
console.log(textResult);


